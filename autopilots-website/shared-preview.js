const main = document.querySelector("main");
if (main && !document.querySelector(".ap-shared-logo-wrap") && !document.querySelector(".ap-shared-autofill")) {
  const finalSection = main.querySelector(".ap-final, .ap-final-box")?.closest("section");
  const template = document.createElement("template");
  template.innerHTML = sharedSections + contactSection;
  main.insertBefore(template.content, finalSection || null);
} else if (main && !document.querySelector(".ap-shared-contact-grid")) {
  const finalSection = main.querySelector(".ap-final, .ap-final-box")?.closest("section");
  const template = document.createElement("template");
  template.innerHTML = contactSection;
  main.insertBefore(template.content, finalSection || null);
}

const collabsSection = main?.querySelector(".ap-collabs-section");
const teamSection = main?.querySelector(".ap-team-section");
if (main && collabsSection && teamSection && (collabsSection.compareDocumentPosition(teamSection) & Node.DOCUMENT_POSITION_FOLLOWING)) {
  teamSection.insertAdjacentElement("afterend", collabsSection);
}

document.querySelectorAll("[data-ap-slide-next]").forEach((button) => {
  button.addEventListener("click", () => {
    const slider = document.querySelector(`[data-ap-slider="${button.dataset.apSlideNext}"]`);
    if (slider) slider.scrollBy({ left: 280, behavior: "smooth" });
  });
});

document.querySelectorAll("[data-ap-slide-prev]").forEach((button) => {
  button.addEventListener("click", () => {
    const slider = document.querySelector(`[data-ap-slider="${button.dataset.apSlidePrev}"]`);
    if (slider) slider.scrollBy({ left: -280, behavior: "smooth" });
  });
});
